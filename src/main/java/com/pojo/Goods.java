package com.pojo;

public class Goods {
	
	private String goodsId;			//商品ID
	private String goodsNo;			//商品编号
	private String goodsName;		//商品名称
	private String typeId;			//商品分类ID
	private String typeName;		//商品分类名称
	private String price;			//价格
	private String stock;			//库存数量
	private String timeSale;		//起售时间
	private String image;			//商品图片
	private String detail;			//商品简介
	private String timeRenew;		//更新时间
	
	
	public String getGoodsId() {
		return goodsId;
	}
	public void setGoodsId(String goodsId) {
		this.goodsId = goodsId;
	}
	public String getGoodsNo() {
		return goodsNo;
	}
	public void setGoodsNo(String goodsNo) {
		this.goodsNo = goodsNo;
	}
	public String getGoodsName() {
		return goodsName;
	}
	public void setGoodsName(String goodsName) {
		this.goodsName = goodsName;
	}
	public String getTypeId() {
		return typeId;
	}
	public void setTypeId(String typeId) {
		this.typeId = typeId;
	}
	public String getTypeName() {
		return typeName;
	}
	public void setTypeName(String typeName) {
		this.typeName = typeName;
	}
	public String getPrice() {
		return price;
	}
	public void setPrice(String price) {
		this.price = price;
	}
	public String getStock() {
		return stock;
	}
	public void setStock(String stock) {
		this.stock = stock;
	}
	public String getTimeSale() {
		return timeSale;
	}
	public void setTimeSale(String timeSale) {
		this.timeSale = timeSale;
	}
	public String getImage() {
		return image;
	}
	public void setImage(String image) {
		this.image = image;
	}
	public String getDetail() {
		return detail;
	}
	public void setDetail(String detail) {
		this.detail = detail;
	}
	public String getTimeRenew() {
		return timeRenew;
	}
	public void setTimeRenew(String timeRenew) {
		this.timeRenew = timeRenew;
	}
}
